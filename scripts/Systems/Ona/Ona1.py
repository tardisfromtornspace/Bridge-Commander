def LoadBackdrops(pSet):

	#Draw order is implicit. First object gets drawn first

	# Backdrop Sphere "Backdrop galaxy3 3"
	kThis = App.BackdropSphere_Create()
	kThis.SetName("Backdrop galaxy3 3")
	kThis.SetTranslateXYZ(0.000000, 0.000000, 0.000000)
	kForward = App.TGPoint3()
	kForward.SetXYZ(0.020445, -0.998608, 0.048622)
	kUp = App.TGPoint3()
	kUp.SetXYZ(-0.281289, 0.040922, 0.958750)
	kThis.AlignToVectors(kForward, kUp)
	kThis.SetTextureFileName("data/Backgrounds/galaxy3.tga")
	kThis.SetTargetPolyCount(25)
	kThis.SetHorizontalSpan(0.041693)
	kThis.SetVerticalSpan(0.075047)
	kThis.SetSphereRadius(300.000000)
	kThis.SetTextureHTile(1.000000)
	kThis.SetTextureVTile(1.000000)
	kThis.Rebuild()
	pSet.AddBackdropToSet(kThis,"Backdrop galaxy3 3")
	kThis.Update(0)
	kThis = None
	# End Backdrop Sphere "Backdrop galaxy3 3"

	# Star Sphere "Backdrop stars"
	kThis = App.StarSphere_Create()
	kThis.SetName("Backdrop stars")

def Initialize():
	# Create the set ("CJones9")
	pSet = App.SetClass_Create()
	App.g_kSetManager.AddSet(pSet, "CJones9")

	# Save the name of the region file that's creating the set.
	pSet.SetRegionModule("Systems.CJones.CJones9")

	# Activate the proximity manager for our set.
	pSet.SetProximityManagerActive(1)

	# Load the placements and backdrops for this set.
	LoadPlacements("CJones9")
	LoadBackdrops(pSet)

	#Load and place the grid.
	pGrid = App.GridClass_Create()
	pSet.AddObjectToSet(pGrid, "grid")
	pGrid.SetHidden(1)

	# Create static objects for this set:
	# If you want to create static objects for this region, make a
	# "CJones9_S.py" file with an Initialize function that creates them.
	try:
		import CJones9_S
		CJones9_S.Initialize(pSet)
	except ImportError:
		# Couldn't find the file.  That's ok.  Do nothing...
		pass

	# Done.

